/*
Mayor que: a > b           a es mayor que b
Menor que: a < b           a es menor que b
Mayor o igual que: a >= b  a es mayor o igual que b
Menor o igual que: a <= b  a es menor o igual que b
Igual: a == b              a es igual que b    
Distinto: a != b           a es distinto que b
Operador estricto: ===
Operador diferente estricto: !==
*/

let n1 = 22;
let n2 = 15;

console.log(n1 > n2)
console.log(n1 < n2)
console.log(n1 >= n2)
console.log(n1 <= n2)
console.log(n1 == n2)
console.log(n1 !== n2)

// AND && Sólo es verdadero si ambos son verdaderos

// OR || con que uno de los dos sea verdadero es verdadero

// NOT (!) cuando está afuera afecta a toda la operación adentro del paréntesis. Cuando está adentro del paréntesis, afecta solo a la expresión que está a su derecha.




